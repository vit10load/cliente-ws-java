    private void bt_calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_calcularActionPerformed
        
        try {
            
            String sexo = calculaImcObject.getSexo(this.sexo.getText());
            this.lb_sexo.setText(sexo);
            double imc = calculaImcObject.getImc(Double.parseDouble(this.peso.getText()),
                    Double.parseDouble(this.altura.getText()));
            this.lb_imc.setText(String.valueOf(imc));
            
        }catch(NumberFormatException e){
            
            System.out.println("err.."+e.getMessage());
        }
        
        
    }//GEN-LAST:event_bt_calcularActionPerformed

